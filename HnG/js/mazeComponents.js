 class GretelTop {
     constructor(ctx) {
         this.gretel = new Image();
         this.gretel.src = 'images/gretel-from-top.png';

         this.x = 0;
         this.y = 0;

         this.gretel.onload = () => {
             ctx.drawImage(this.gretel, this.x, this.y, 26, 26);
         }


     }

     gretelUp() {
         let isCollide = checkCollision(this.x, this.y);
         if (this.y > 0 && isCollide === false) {
             this.dy = -5;
             this.dx = 0;
         } else {
             this.dy = 0;
             this.dx = 0;
         }
     }

     gretelDown() {
         let isCollide = checkCollision(this.x, this.y + 26);
         if (this.y < 700 && isCollide === false) {
             this.dy = 5;
             this.dx = 0;
         } else {
             this.dy = 0;
             this.dx = 0;
         }
     }

     gretelRight() {
         let isCollide = checkCollision(this.x + 26, this.y);
         if (this.x < 970 && isCollide === false) {
             this.dx = 5;
             this.dy = 0;
         } else {
             this.dy = 0;
             this.dx = 0;
         }
     }

     gretelLeft() {
         let isCollide = checkCollision(this.x, this.y);
         if (this.x > 0 && isCollide === false) {
             this.dx = -5;
             this.dy = 0;
         } else {
             this.dy = 0;
             this.dx = 0;
         }
     }


     gretelUpdatePos() {

         if (this.x < (450 + 50) && this.y < (340 + 51) && (this.y + 26) > 340 && (this.x + 26) > 450) {
             screen.hat.hat.src = 'images/blank.png';
             screen.hat.state = 1;
         }

         if (this.x < 10 && this.y < 10 && screen.hat.state === 1) {
             exitCanvas();

         }
         this.x = this.x + this.dx;
         this.y = this.y + this.dy;
     }
 }

